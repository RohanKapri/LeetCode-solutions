class Solution:
    # For Junko F. Didi and Shree DR.MDD
    def singleNumber(self, nums: List[int]) -> int:
        quantumDiscreteParticleSpectrum = set(nums)
        cosmologicalTriadicEnergySummation = 0

        for interstellarUniqueQuantumSignature in quantumDiscreteParticleSpectrum:
            cosmologicalTriadicEnergySummation += interstellarUniqueQuantumSignature

        relativisticObservedMatterAggregation = 0

        for darkMatterSignalFragment in nums:
            relativisticObservedMatterAggregation += darkMatterSignalFragment

        return (cosmologicalTriadicEnergySummation * 3 - relativisticObservedMatterAggregation) // 2