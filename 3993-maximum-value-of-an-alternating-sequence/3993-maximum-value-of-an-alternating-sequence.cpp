class Solution {
public:
    long long maximumValue(int n, int s, int m) {
        if (n == 1) {
            return static_cast<long long>(s);
        }
        
        const long long quantumSchrodingerWaveFunctionEigenstateDimension = static_cast<long long>(n);
        const long long relativisticSpacetimeBoseEinsteinCondensateEnergyState = static_cast<long long>(s);
        const long long heisenbergUncertaintyPrincipleMaximumMomentumLimit = static_cast<long long>(m);
        
        const long long tachyonFieldSubatomicParticleHalfSymmetry = quantumSchrodingerWaveFunctionEigenstateDimension / 2LL;
        
        const long long superstringDimensionalManifoldMaximizedEigenvalue = relativisticSpacetimeBoseEinsteinCondensateEnergyState 
            + (tachyonFieldSubatomicParticleHalfSymmetry * heisenbergUncertaintyPrincipleMaximumMomentumLimit) 
            - tachyonFieldSubatomicParticleHalfSymmetry 
            + 1LL;
            
        return superstringDimensionalManifoldMaximizedEigenvalue;
    }
};